# Creating three different dictionaries
dict1 = {'a': 1, 'b': 2}
dict2 = {'c': 3, 'd': 4}
dict3 = {'e': 5, 'f': 6}

# Merging dictionaries using the ** operator
merged_dict1 = {**dict1, **dict2, **dict3}

# Merging dictionaries using the | operator (Python 3.9+)
merged_dict2 = dict1 | dict2 | dict3

print(merged_dict1)
print(merged_dict2)

